public class HWVariables {
    public static void main(String[] args) {

        byte MyByte1;
        MyByte1=100;

        byte MyByte2;
        MyByte2=-10;

        System.out.println("Переменная MyByte1 = " + MyByte1);
        System.out.println("Переменная MyByte2 = " + MyByte2);

        short MyShort1;
        MyShort1=23456;

        short MyShort2;
        MyShort2=0;

        System.out.println("Переменная MyShort1 = " + MyShort1);
        System.out.println("Переменная MyShort2 = " + MyShort2);

        int MyInt1;
        MyInt1=1098237;

        int MyInt2;
        MyInt2=-1298372;

        System.out.println("Переменная MyInt1 = " + MyInt1);
        System.out.println("Переменная MyInt2 = " + MyInt2);

        long MyLong1;
        MyLong1=12093746354L;

        long MyLong2;
        MyLong2=-12937454362372923L;

        System.out.println("Переменная MyLong1 = " + MyLong1);
        System.out.println("Переменная MyLong2 = " + MyLong2);

        float MyFloat1;
        MyFloat1=2.389f;

        float MyFloat2;
        MyFloat2=-1897.23f;

        System.out.println("Переменная MyFloat1 = " + MyFloat1);
        System.out.println("Переменная MyFloat2 = " + MyFloat2);

        double MyDouble1;
        MyDouble1=-126738.12789328198d;

        double MyDouble2;
        MyDouble2=32489128.123;

        System.out.println("Переменная MyDouble1 = " + MyDouble1);
        System.out.println("Переменная MyDouble2 = " + MyDouble2);

        boolean MyBoolean1;
        MyBoolean1=true;

        boolean MyBoolean2;
        MyBoolean2=false;

        System.out.println("Переменная MyBoolean1 = " + MyBoolean1);
        System.out.println("Переменная MyBoolean2 = " + MyBoolean2);

        char MyChar1;
        MyChar1='1';

        char MyChar2;
        MyChar2='\u2589';

        System.out.println("Переменная MyChar1 = " + MyChar1);
        System.out.println("Переменная MyChar2 = " + MyChar2);


    }
}
